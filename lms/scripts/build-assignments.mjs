/**
 * Build assignment definitions from lms/worksheets/*.html
 * Run: node scripts/build-assignments.mjs
 */
import { readFileSync, writeFileSync, readdirSync } from 'fs'
import { join, dirname } from 'path'
import { fileURLToPath } from 'url'

const root = join(dirname(fileURLToPath(import.meta.url)), '..')
const worksheetsDir = join(root, 'worksheets')
const outFile = join(root, 'src', 'content', 'assignments.generated.ts')

const CONFIDENCE_OPTIONS = [
  'Not confident at all: I need to learn more',
  'Somewhat confident: I know the basics',
  'Confident: I practise safety consistently',
  'Very confident: I can teach others',
]

/** Prefer plain punctuation over em dashes in learner-facing copy. */
function plainPunctuation(s) {
  return s.replace(/\s*\u2014\s*/g, ', ').replace(/\s{2,}/g, ' ').trim()
}

function stripHtml(s) {
  return plainPunctuation(s.replace(/<[^>]+>/g, '').replace(/\s+/g, ' ').trim())
}

function parseWorksheet(filename) {
  const html = readFileSync(join(worksheetsDir, filename), 'utf8')
  const id = filename.replace(/\.html$/, '')

  const titleMatch = html.match(/<div class="header-title">([^<]+)<\/div>/)
  const title = titleMatch?.[1]?.trim() ?? id

  const tradeIdMatch = html.match(/trade_id:\s*"(\d+)"/)
  const sortOrder = tradeIdMatch ? parseInt(tradeIdMatch[1], 10) : 0

  const skillNames = [...html.matchAll(/<div class="skill-name">([^<]+)<\/div>/g)].map((m) =>
    plainPunctuation(m[1].trim()),
  )

  // Skip the instructional "Read this situation carefully…" label. It has no answer box
  // and was shifting every later question onto the wrong control.
  const qLabels = [...html.matchAll(/<div class="q-label"[^>]*>([\s\S]*?)<\/div>/g)]
    .map((m) => stripHtml(m[1]))
    .filter((label) => !/read this situation carefully/i.test(label))

  const scenarioMatch = html.match(
    /<div class="def-box" style="margin-bottom:14px; font-style:italic;">([\s\S]*?)<\/div>/,
  )
  const scenarioContext = scenarioMatch ? stripHtml(scenarioMatch[1]) : ''

  const definitionMatch = html.match(
    /<div class="def-label">Definition<\/div>\s*([\s\S]*?)<\/div>\s*<div class="q-label"/,
  )
  const definitionText = definitionMatch ? stripHtml(definitionMatch[1]) : ''

  const questions = [
    {
      id: 'master_name',
      label: "Your master's name",
      type: 'text',
      required: false,
      section: 'About you',
    },
    {
      id: 'q1_definition',
      label: qLabels[0] ?? 'In your own words, explain what this trade means.',
      type: 'textarea',
      required: true,
      section: 'Definition',
      context: definitionText || undefined,
    },
    {
      id: 'q2a_skills_checked',
      label: qLabels[1] ?? 'Which skills do you already practise?',
      type: 'checkbox',
      required: false,
      options: skillNames,
      section: 'Technical skills',
    },
    {
      id: 'q2b_skill_practice',
      label: qLabels[2] ?? 'Choose ONE skill and describe how you do it.',
      type: 'textarea',
      required: true,
      section: 'Technical skills',
    },
    {
      id: 'q3_scenario',
      label: qLabels[3] ?? 'What is your answer?',
      type: 'textarea',
      required: true,
      section: 'Apply your knowledge',
      context: scenarioContext || undefined,
    },
    {
      id: 'q4_safety',
      label: qLabels[4] ?? 'Safety and hygiene question',
      type: 'textarea',
      required: true,
      section: 'Safety & hygiene',
    },
    {
      id: 'q4_confidence',
      label: qLabels[5] ?? 'How confident are you with safety practices?',
      type: 'radio',
      required: false,
      options: CONFIDENCE_OPTIONS,
      section: 'Safety & hygiene',
    },
    {
      id: 'q5a_commitment',
      label: 'Name ONE specific skill you will work on this week.',
      type: 'textarea',
      required: true,
      section: 'My commitment',
    },
    {
      id: 'q5b_improvement',
      label: qLabels[6] ?? 'How will you know you have improved?',
      type: 'textarea',
      required: false,
      section: 'My commitment',
    },
    {
      id: 'q5c_cross_trade',
      label: qLabels[7] ?? 'Which skill from another trade impressed you?',
      type: 'textarea',
      required: false,
      section: 'My commitment',
    },
  ]

  return {
    id,
    title,
    // Preview cards show the trade definition, not a repeated title line.
    description: definitionText || `EIF Programme worksheet for ${title}`,
    sortOrder,
    published: true,
    questions,
  }
}

const files = readdirSync(worksheetsDir).filter((f) => f.endsWith('.html')).sort()
const assignments = files.map(parseWorksheet)

const ts = `/** Auto-generated by scripts/build-assignments.mjs — do not edit by hand */
import type { Assignment } from '@/types'

export const generatedAssignments: Assignment[] = ${JSON.stringify(assignments, null, 2)} as Assignment[]
`

writeFileSync(outFile, ts, 'utf8')
console.log(`Wrote ${assignments.length} assignments to ${outFile}`)
